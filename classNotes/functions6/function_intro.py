'''         Functions
A function is a named block of code that preforms some action.
A function is like a mini-program within a program.
They encapsulate pieces of code to perform specified operations.
Functions are used to perform a similar kind of tasks again and again without writing the same code again.

    Advantages
1. Reusability
2. Easier to modify
3. Easier to maintain
3. Easier to understand
5. Consise code
6. Less time

    Syntax
def function_Name(arguments/parameters):
    statements(s)
    
                    Example:
def hello():
    print('hello world')

hello()

'''
